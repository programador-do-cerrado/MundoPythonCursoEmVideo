"""
Faça um programa que um numero inteiro em ostre na tela seu sucessor e seu antecessor
"""

n1 = int(input('Digite um número:'))
antecessor = n1 - 1;
sucessor = n1 + 1;
print('O numero digitado foi: {}, seu antecessor é: {} e seu sucessor é: {}'.format(n1, antecessor, sucessor))
print('O numero digitado foi: {}, seu antecessor é: {} e seu sucessor é: {}'.format(n1, (n1 - 1), (n1 + 1)))
